    <h2>Elon Musk</h2>
		<div class="description">Inventor | Investor | Engineer</div>

        <h3>Bio</h3>
        <figure class = "fig1">
            <img src="./images/elonmusk.png" alt = "Elon Musk">
            <figcaption>Elon Musk</figcaption>
        </figure>

        <p>Musk is the founder, CEO, and CTO of SpaceX; a co-founder, a Series A investor, CEO, and product architect of Tesla
            Inc.; co-chairman of OpenAI; founder and CEO of Neuralink. He was previously co-founder and chairman of SolarCity;
            co-founder of Zip2; and founder of X.com, which merged with Confinity and took the name PayPal. As of July 2017,
            he has an estimated net worth of $16.1 billion, making him the 80th-wealthiest person in the world. In December
            2016, Musk was ranked 21st on the Forbes list of The World's Most Powerful People.</p>

        
            <p>Musk has stated that the goals of SolarCity, Tesla, and SpaceX revolve around his vision to change the world and
            humanity. His goals include reducing global warming through sustainable energy production and consumption, and
            reducing the "risk of human extinction" by "making life multiplanetary" by establishing a human colony on Mars.</p>

            <p>In addition to his primary business pursuits, he has also envisioned a high-speed transportation system known as
            the Hyperloop, and has proposed a VTOL supersonic jet aircraft with electric fan propulsion, known as the Musk
            electric jet.</p>

        <h3>Education</h3>
        <figure>
            <img src="./images/elonmusk1.jpg">
            <figcaption>A Younger Elon Musk</figcaption>
        </figure>
        <p>At the age of 17, Musk was accepted into Queen's University in Kingston, Ontario, for undergraduate study. In 1992,
            after spending two years at Queen's University, Musk transferred to the University of Pennsylvania, where in
            May 1997 he received a Bachelor of Science degree in physics from its College of Arts and Sciences, and a Bachelor
            of Science degree in economics from its Wharton School of Business. Musk had extended his studies for one year
            to finish the second bachelor's degree. While at the University of Pennsylvania, Musk and fellow Penn student
            Adeo Ressi rented a 10-bedroom fraternity house, using it as an unofficial nightclub.</p>
        <p>In 1995, at age 24, Musk moved to California to begin a PhD in applied physics and materials science at Stanford
            University, but left the program after two days to pursue his entrepreneurial aspirations in the areas of the
            Internet, renewable energy and outer space. In 2002, he became a U.S. citizen.</p>


        <h3>Career</h3>
        <h4>X.com and PayPal</h4>
        <p><a href="https://paypal.com">PayPal website</a></p>
        <p>In March 1999, Musk co-founded X.com, an online financial services and e-mail payment company, with US$10 million
            from the sale of Zip2. One year later, the company merged with Confinity, which had a money transfer service
            called PayPal. The merged company focused on the PayPal service and was renamed PayPal in 2001. PayPal's early
            growth was driven mainly by a viral marketing campaign where new customers were recruited when they received
            money through the service. Musk was ousted in October 2000 from his role as CEO (although he remained on the
            board) due to disagreements with other company leadership, notably over his desire to move PayPal's Unix-based
            infrastructure to Microsoft Windows. In October 2002, PayPal was acquired by eBay for US$1.5 billion in stock,
            of which Musk received US$165 million. Before its sale, Musk, who was the company's largest shareholder, owned
            11.7% of PayPal's shares.</p>
        <h4>SpaceX</h4>
        <p><em><a href="http://www.spacex.com">Spacex website</a></em></p>
        <p>In 2001, Musk conceptualized "Mars Oasis"; a project to land a miniature experimental greenhouse on Mars, containing
            food crops growing on Martian regolith, in an attempt to regain public interest in space exploration. In October
            2001, Musk travelled to Moscow with Jim Cantrell (an aerospace supplies fixer), and Adeo Ressi (his best friend
            from college), to buy refurbished Dnepr Intercontinental ballistic missiles (ICBMs) that could send the envisioned
            payloads into space. The group met with companies such as NPO Lavochkin and Kosmotras; however, according to
            Cantrell, Musk was seen as a novice and was consequently spat on by one of the Russian chief designers, and the
            group returned to the United States empty-handed. In February 2002, the group returned to Russia to look for
            three ICBMs, bringing along Mike Griffin, who had worked for the CIA's venture capital arm, In-Q-Tel; NASA's
            Jet Propulsion Laboratory; and was just leaving Orbital Sciences, a maker of satellites and spacecraft. The group
            met again with Kosmotras, and were offered one rocket for US$8 million, however, this was seen by Musk as too
            expensive; Musk consequently stormed out of the meeting. On the flight back from Moscow, Musk realized that he
            could start a company that could build the affordable rockets he needed. According to early Tesla and SpaceX
            investor Steve Jurvetson, Musk calculated that the raw materials for building a rocket actually were only 3 percent
            of the sales price of a rocket at the time. It was concluded that theoretically, by applying vertical integration
            and the modular approach from software engineering, SpaceX could cut launch price by a factor of ten and still
            enjoy a 70-percent gross margin. Ultimately, Musk ended up founding SpaceX with the long-term goal of creating
            a "true spacefaring civilization".</p>
        <p>SpaceX is both the largest private producer of rocket engines in the world, and holder of the record for highest
            thrust-to-weight ratio for a rocket engine. SpaceX has produced more than 100 operational Merlin 1D engines,
            currently the world's most powerful engine for its weight. The relatively immense power to weight ratio allows
            each Merlin 1D engine to vertically lift the weight of 40 average family cars. In combination, the 9 Merlin engines
            in the Falcon 9 first stage produce anywhere from 5.8 to 6.7 MN (1.3 to 1.5 million pounds) of thrust, depending
            on altitude.</p>
        <h4>Tesla</h4>
        <p><em><a href="https://www.tesla.com/">Tesla website</a></em></p>
        <p>Tesla, Inc. was incorporated in July 2003 by Martin Eberhard and Marc Tarpenning, who financed the company until
            the Series A round of funding.</p>
        <p>Both men played active roles in the company's early development prior to Elon Musk's involvement. Musk led the Series
            A round of investment in February 2004, joining Tesla's board of directors as its chairman. Musk took an active
            role within the company and oversaw Roadster product design at a detailed level, but was not deeply involved
            in day-to-day business operations.</p>
        <p>Following the financial crisis in 2008, Musk assumed leadership of the company as CEO and product architect, positions
            he still holds today. Tesla Motors first built an electric sports car, the Tesla Roadster in 2008, with sales
            of about 2,500 vehicles to 31 countries. Tesla began delivery of its four-door Model S sedan on June 22, 2012.
            It unveiled its third product, the Model X, aimed at the SUV/minivan market, on February 9, 2012; the Model X
            launch was however delayed until September 2015. In addition to its own cars, Tesla sells electric powertrain
            systems to Daimler for the Smart EV, Mercedes B-Class Electric Drive and Mercedes A Class and to Toyota for the
            RAV4 EV. Musk was able to bring in both companies as long-term investors in Tesla.</p>

        <h4>Hyperloop</h4>
        <p><em><a href="https://hyperloop-one.com/">Hyperloop website</a></em></p>
        <p>On August 12, 2013, Musk unveiled a concept for a high-speed transportation system incorporating reduced-pressure
            tubes in which pressurized capsules ride on an air cushion driven by linear induction motors and air compressors.
            The mechanism for releasing the concept was an alpha-design document that, in addition to scoping out the technology,
            outlined a notional route where such a transport system might be built: between the Greater Los Angeles Area
            and the San Francisco Bay Area.</p>
        <p>After earlier envisioning Hyperloop, Musk assigned a dozen engineers from Tesla and SpaceX who worked for nine months,
            establishing the conceptual foundations and creating the designs for the transportation system. An early design
            for the system was then published in a whitepaper posted to the Tesla and SpaceX blogs. Musk's proposal, if technologically
            feasible at the costs he has cited, would make Hyperloop travel cheaper than any other mode of transport for
            such long distances. The alpha design was proposed to use a partial vacuum to reduce aerodynamic drag, which
            it is theorized would allow for high-speed travel with relatively low power, with certain other features like
            air-bearing skis and an inlet compressor to reduce air drag. The alpha design document estimated the total cost
            of an LA-to-SF Hyperloop system at US$6 billion, but this amount is speculative.</p>
        <p>In June 2015, Musk announced a design competition for students and others to build Hyperloop pods to operate on a
            SpaceX-sponsored mile-long track in a 2015–2017 Hyperloop pod competition. The track was used in January 2017,
            and Musk also started building a tunnel.</p>

